public class Calculator{
    public static int sum(int a, int b) {
        return a+b;
    }
    public static int minus(int a, int b){
        return a-b;
    }
    public static int mul(int a, int b){
        return a*b;
    }
    public static int sum(int a, int b, int c) {
        return a+b+c;
    }
}